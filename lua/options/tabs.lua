local tab_spaces = 2
-------------------------------------------------------------------
-- Tab
-------------------------------------------------------------------
vim.opt.tabstop = tab_spaces          -- number of spaces that a <Tab> in the file counts for
vim.opt.shiftwidth = tab_spaces       -- number of spaces to use for each step of (auto) indent
vim.opt.softtabstop = tab_spaces      -- same as tabstop, but its for editing operation
vim.opt.expandtab = true              -- set whitespace as tab
